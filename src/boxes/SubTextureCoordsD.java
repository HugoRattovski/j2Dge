package boxes;

public class SubTextureCoordsD {

    public double upperLeftX;
    public double upperLeftY;
    public double lowerRightX;
    public double lowerRightY;

    public SubTextureCoordsD(double ulX, double ulY, double lrX, double lrY) {
        this.upperLeftX = ulX;
        this.upperLeftY = ulY;
        this.lowerRightX = lrX;
        this.lowerRightY = lrY;
    }
}
